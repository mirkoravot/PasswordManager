    public void initDialog() {
        this.jTextFieldAccountName.setText(account.getAttribute("name"));
        this.jTextFieldCurrentPassword.setText(account.getAttribute("password"));
        tableModel = new PasswordHistoryTableModel(null);
        this.jTable1.setModel(tableModel);
        updateTableModel();
        this.jTable1.setVisible(false);
        this.setTitle("Account Password: " + account.getAttribute("name"));
    }
    
    public void updateTableModel() {
        try  {
            NodeList list = account.getElementsByTagName("PasswordChange");

            ArrayList alist = new ArrayList();
            for (int i = 0; i < list.getLength(); i++) {
                System.out.println(list.item(i));
                alist.add(list.item(i));
            }
            tableModel = new PasswordHistoryTableModel(alist);
            alist.sort(new AccountComparator());
            this.jTable1.setModel(tableModel);

        } catch (Exception ex) {
            ex.printStackTrace();
            
        }
        
    }
