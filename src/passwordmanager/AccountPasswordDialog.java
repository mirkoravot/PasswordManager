    private void generatePassword() {
        String randomPassword = PasswordGenerator.generateRandomPassword(10);
        this.jPasswordField1.setText(randomPassword);
        this.showCleanPassword();
    }
    
    private void showCleanPassword() {
        this.jTextFieldCleanPassword.setText(new String(this.jPasswordField1.getPassword()));
    }
    
    private void changePassword() {
        String newPassword = new String(this.jPasswordField1.getPassword());
        if (newPassword.equals(account.getAttribute("password"))) {
            console.showSimpleExceptionDialog(new Exception("New password equals to old password"));
            return;
        }
        this.account.setAttribute("password", newPassword);
        this.jTextFieldCurrentPassword.setText(newPassword);
        Element passwordChange = console.getDatabase().getDocument().createElement("PasswordChange");
        passwordChange.setAttribute("date", "" + Calendar.getInstance().getTimeInMillis());
        passwordChange.setAttribute("password", newPassword);
        account.appendChild(passwordChange);
        this.updateTableModel();
        this.jButtonCancel.setEnabled(false);
        showCleanPassword();
        
    }

